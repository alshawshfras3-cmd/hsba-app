// Simple normalization helper for Arabic search queries
function normalizeArabic(text: string): string {
  if (!text) return '';
  return text
    .replace(/[أإآأ]/g, 'ا')
    .replace(/ة/g, 'ه')
    .replace(/ى/g, 'ي')
    .replace(/[\u064B-\u065F]/g, '') // remove diacritics
    .trim()
    .toLowerCase();
}

// 2. Knowledge search logic (returns local responses based on keywords)
export function getAssistantResponse(mode: 'customer' | 'admin', question: string): string {
  const norm = normalizeArabic(question);

  if (mode === 'customer') {
    // 1. DSR (استقطاع / خصم / نسبه)
    if (norm.includes('استقطاع') || norm.includes('نسبه') || norm.includes('dsr') || norm.includes('خصم') || norm.includes('مسموح')) {
      return `نسبة الاستقطاع (DSR) هي الحد الأقصى المسموح بخصمه من صافي راتب العميل لصالح الأقساط الشهرية. 
      • يتم احتسابها تلقائيًا بناءً على قطاع العمل وقيمة الراتب.
      • القسط الإجمالي (العقاري + الشخصي إن وجد) لا يمكن أن يتجاوز هذه النسبة المعتمدة (مثلاً 55% أو 65%) طبقًا لسياسات البنك المركزي السعودي ولوائح البنوك المنفردة لجعل التمويل ملائمًا للقدرة الائتمانية.`;
    }

    // 2. Explanation of results (شرح / نتائج / حبل / ارقام)
    if (norm.includes('شرح') || norm.includes('نتائج') || norm.includes('تفاصيل') || norm.includes('ارقام') || norm.includes('الحسبه')) {
      return `إليك كيفية قراءة تفاصيل الحسبة المعروضة:
      • إجمالي التمويل المتاح: يمثل مجموع التمويل العقاري الأقصى المتاح بالإضافة للتمويل الشخصي إذا كان المنتج يدعم ذلك.
      • الدعم السكني: قيمة الدعم المالي غير المسترد المقدم من صندوق التنمية العقارية (تلقائي الحساب لخدمة مستفيدي سكني).
      • القسط الشهري الإجمالي: القسط الملتزم بسداده طوال فترة التمويل الحالية، مقسمًا بين العقاري والشخصي.
      • القسط بعد انتهاء الشخصي: ينخفض قسط سدادك تلقائياً فور انتهاء مدة سداد التمويل الشخصي (مثال: بعد 5 سنوات أو 60 شهراً)، ليبقى القسط العقاري فقط.`;
    }

    // 3. Housing Support types (دعم / شهري / دفعه / باقه)
    if (norm.includes('دعم') || norm.includes('سكني') || norm.includes('شهري') || norm.includes('دفعه') || norm.includes('مسبق')) {
      return `يتم احتساب الدعم بناءً على مصفوفة الدعم السكني الجديدة:
      • الدعم الشهري: مبلغ مالي ثابت يتم إضافته لراتبك شهريًا لتغطية جزء من أرباح التمويل العقاري على مدار مدة العقد.
      • دعم الدفعة المسبقة: تقديم مبلغ الدعم دفعة واحدة غير مستردة عند التوقيع لخفض قيمة الدفعة الأولى أو شراء العقار (بحدود 100 ألف أو 150 ألف ريال). 
      • يطبق النظام الخيار الأفضل والأكثر ملاءمة تلقائياً حسب الفئة المستحقة للدعم المالي للتسهيل عليك.`;
    }

    // 4. Bank differences (بنوك / بنك / اختلاف / مبالغ / فروقات)
    if (norm.includes('بنك') || norm.includes('بنوك') || norm.includes('سعر') || norm.includes('اختلاف') || norm.includes('فرق')) {
      return `تختلف مبالغ التمويل القصوى والفوائد والحلول المطروحة بين البنوك لعدة أسباب:
      • تفاوت هوامش الأرباح والرسوم الإدارية بين جهة وأخرى.
      • اختلاف حد الراتب الأدنى المشترط للتمكين من الأهلية.
      • معايير قبول قطاعات التنمية الخاصة بجهات العمل (عسكري، حكومي، مدني، قطاع خاص معتمد أو غير معتمد).
      • لوائح احتساب DSR الحصري لكل بنك لتطبييق سياسات وقبول مرونة إضافية.`;
    }

    // 5. Retirement (تقاعد / عمر / سن / تناقص / مده)
    if (norm.includes('تقاعد') || norm.includes('عمر') || norm.includes('سن') || norm.includes('شرائح') || norm.includes('مده')) {
      return `مدة التمويل الشخصي أو العقاري يتم معالجتها وتقييدها تلقائيًا بناءً على قواعد دقيقة في التطبيق:
      • لا تشمل المدة سنوات تتعدى السن الأقصى لسن الخدمة أو التقاعد المعتمد (مثل 60 أو 65 سنة للبدنيين/العسكريين).
      • في حال كان العميل قريبًا من التقاعد، يتم تقليص أو حصر مدة التمويل الشخصي في الفترة السابقة للتقاعد لمنع تداخل الاستقطاع، وتطبيق تناقص قسط تلقائي آمن فيما بعد.`;
    }

    // 6. Personal missing (شخصي / غير متوفر / مفقود / تمويل شخصي)
    if (norm.includes('شخصي') || norm.includes('غير متوفر') || norm.includes('مفقود') || norm.includes('لا يظهر')) {
      return `قد تظهر عبارة "التمويل الشخصي غير متوفر لدى هذه الجهة" أو لا يحتسب لأحد الأسباب التالية:
      • أن المنتج المختار حالياً لا يدعم الدمج (مثل تمويل عقاري قائم فقط بدون دمج تمويل جديد).
      • تجاوز العميل للسن الأقصى المسموح به لمنح التمويل الشخصي في سياسة البنك.
      • عدم توفر قواعد ائتمانية مفعلة مخصصة للتمويل الشخصي لهذا القطاع أو البنك المختار في لوحة تحكم النظام.`;
    }

    // 7. Is it final (نهائي / اكيد / تقديم / حقيقي / مضمون)
    if (norm.includes('نهائي') || norm.includes('اكيد') || norm.includes('تقديم') || norm.includes('حقيقي') || norm.includes('مضمون') || norm.includes('تعهد') || norm.includes('معتمد')) {
      return `نود التنويه دائمًا بأن جميع أرقام الحسبة المستخرجة عبر المنصة هي تقديرية ومبدئية لبناء مقارنة عامة وتوضيحية للقرارات التمويلية. 
      • لا تشكل هذه الحسبة عرضًا ملزمًا أو موافقة ائتمانية مضمونة.
      • الموافقة النهائية والشروط الدقيقة تخضع للدراسة الائتمانية الرسمية وسجل سمة (SIMAH) لدى البانك أو الجهة المقرضة فور التقديم الفعلي.`;
    }

    // Customer Fallback search results
    return `لم أفهم تمامًا سؤالك بخصوص الحسبة المالية. يمكنك اختيار أحد المواضيع المقترحة بالأسفل لمعرفة المزيد من التفاصيل التوضيحية أو مراجعة المعايير.`;

  } else {
    // ADMIN ASSISTANT HELPER

    // 1. Margins (هامش / هوامش / ارباح / ربح)
    if (norm.includes('هامش') || norm.includes('هوامش') || norm.includes('ربح') || norm.includes('ارباح') || norm.includes('نسبه')) {
      return `إعدادات هوامش الربح السنوية (Annual Margin %):
      • تمثل النسبة المئوية السنوية التي يتقاضاها البنك كفائدة فوق مبلغ التمويل.
      • يمكنك تعديلها لكل بنك عبر لوحة التحكم بكل سهولة، وسيتم تطبيق النسبة المحدثة فورًا على الحسبة الجديدة للعملاء من خلال محرك التمويل.
      • يرجى مراعاة النطاق السائد في السوق السعودي للمحافظة على واقعية النتائج.`;
    }

    // 2. Table options (جدول / جدولين / تحويل / بدون)
    if (norm.includes('جدول') || norm.includes('موحد') || norm.includes('تحويل') || norm.includes('راتب') || norm.includes('الراتب')) {
      return `ميزة "جدول موحد" مقابل "جدولين":
      • جدول موحد: يطبق نفس قواعد ومصفوفة هوامش الأرباح بغض النظر عما إذا كان العميل سيقوم بتحويل راتبه للبنك أم لا.
      • جدولين منفصلين: يمنحك مرونة لتحديد هوامش مخفضة وجاذبة لمن يلتزمون بـ "تحويل الراتب كلياً"، وهوامش أعلى للعملاء المستفيدين "بدون تحويل الراتب".
      • التبديل بينهما يتم بمرونة تامة وتلقائية عبر لوحة القواعد.`;
    }

    // 3. Salary brackets (فئه / فئات / حد / رواتب / الرواتب)
    if (norm.includes('فئه') || norm.includes('رواتب') || norm.includes('الرواتب') || norm.includes('حد') || norm.includes('ادني')) {
      return `هيكلة فئات الرواتب (Salary Brackets):
      • تتيح لك تصنيف العملاء بناءً على وعاء دخلهم الشهري (مثلاً: أقل من 5000، من 5000 إلى 15000، وهكذا).
      • يتم ربط كل فئة بحد أدنى للأهلية وهوامش أرباح تصاعدية أو تنازلية.
      • يضمن ذلك أن ذوي الدخل المنخفض أو المرتفع توفر لهم البنوك عروضاً متوافقة مع توجيهاتها الداخلية وقطاعها الائتماني.`;
    }

    // 4. Bracket months/years (شرائح / سنين / سنوات / شهور / اشهر)
    if (norm.includes('شرايح') || norm.includes('اشهر') || norm.includes('شهور') || norm.includes('سنوات') || norm.includes('فترات')) {
      return `شرائح الفترات (Tenor Brackets):
      • تمكنك من تخصيص الهامش بناءً على مدة سداد التمويل التراكمية.
      • كقاعدة عامة: قد يرتفع الهامش بشكل نسبي عند اختيار سنوات سداد أطول (مثال: من 20 إلى 25 سنة مقارنة بـ 5 إلى 10 سنوات).
      • يقوم محرك الحسبة بالبحث عن التوافق والبحث في جدول الشريحة المقابلة للفترة تلقائياً.`;
    }

    // 5. Personal finance rules (شخصي / التمويل الشخصي / قواعد / دمج)
    if (norm.includes('شخصي') || norm.includes('قواعد') || norm.includes('تمويل') || norm.includes('اداره')) {
      return `تهيئة منتجات التمويل الشخصي (Personal Finance Rules):
      • يمكنك التحكم في تفعيل ميزة "التمويل الشخصي المدمج" أو إلغائها لبعض البنوك.
      • تحدد هنا الحد الأقصى لمبلغ التمويل الشخصي (مثال: 500,000 ريال) ومدته القصوى (60 شهراً)، بالإضافة إلى رسوم التمويل المطبقة.
      • المحرك سيتحقق من تفعيل الميزة وشروطها قبل السماح بعرض أي تمويل شخصي مدمج للعميل.`;
    }

    // 6. DSR and retirement constraints (قواعد التقاعد / استقطاع / dsr)
    if (norm.includes('استقطاع') || norm.includes('تقاعد') || norm.includes('dsr') || norm.includes('قواعد التقاعد') || norm.includes('محددات')) {
      return `معايير محددات الاستقطاع DSR والتقاعد:
      • حد الاستقطاع يمثل النسبة القصوى لتسديد الأقساط الائتمانية ولا يمكن تجاوزه حرصاً على السياسة الحمائية المصرفية.
      • قواعد التقاعد الصارمة في النظام تضمن عدم تخطي الأقساط لفترة الدخل السلبي أو فترات انقطاع الراتب أو انخفاضه، ويستعين النظام بتصفيد عوائد الأرباح أو تقصير فترات الممنوح الشخصي تلقائياً وفق سياسات البنوك.`;
    }

    // 7. Manual changes reminder (تعديل / تلقائي / يدوي / حفظ / تطبيق)
    if (norm.includes('تعديل') || norm.includes('تلقائي') || norm.includes('يدوي') || norm.includes('حفظ') || norm.includes('تطبيق')) {
      return `تنبيه خاص لمدراء النظام:
      • نظام القواعد مصمم بحيث يكون "قراءة وتوجيه فقط" من جهة المساعد الذكي.
      • المساعد لا يملك أي صلاحيات لحفظ أو تعديل أو تخزين أو مسح أي قاعدة أو هوامش نيابة عنك.
      • يجب إجراء كافة التعديلات وحفظها يدويًا عبر استمارة التحديث وضوابط الإدارة لضمان أمن البيانات والحقول وقواعد الحسبة.`;
    }

    // Admin Fallback response
    return `لم أعثر على فكرة مطابقة تماماً لسؤالك في محيط معايير الإدارة. يمكنك الضغط على أحد عناصر المساعدة الموصى بها في الأسفل للتعرف على الأدلة سريعة الشرح.`;
  }
}
